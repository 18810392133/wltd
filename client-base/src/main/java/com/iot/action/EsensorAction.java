    @AuthToken
    @RequestMapping(value = "/insert")
    public Map<Object, Object> insert(String item, Integer projectid, String protocol, Integer timer, String note) {
        Map<Object, Object> map = new HashMap<Object, Object>();
        if(isValidate(item, projectid, 0)){
            Esensorv sensor = esensorService.insert(item, projectid, protocol, timer, note);
            if(sensor != null)
                map = new BeanMap(sensor);
        }else{
            map.put("isError", true);
            map.put("msg", "该传感器已存在");
        }
        return map;
    }

    @AuthToken
    @RequestMapping(value = "/update")
    public Map<Object, Object> update(Integer id, String item, Integer projectid, String protocol, Integer timer, String note) {
        Map<Object, Object> map = new HashMap<Object, Object>();
        if(isValidate(item, projectid, id)){
            Esensorv sensor = esensorService.update(id, item, projectid, protocol, timer, note);
            if(sensor != null)
                map = new BeanMap(sensor);
        }else{
            map.put("isError", true);
            map.put("msg", "该传感器已存在");
        }
        return map;
    }

    @AuthToken
    @RequestMapping(value = "/delete")
    public Map<Object, Object> delete(Integer id) {
        Map<Object, Object> map = new HashMap<Object, Object>();
        if(esensorService.delete(id) > 0)
            map.put("success", true);
        else
            map.put("success", false);
        return map;
    }

    @AuthToken
    @RequestMapping(value = "/list", method = RequestMethod.GET)
    public JSONArray list(Integer projectid){
        JSONArray jsonArray = new JSONArray();
        List<Esensor> list = esensorService.selectBySql("projectid=" + projectid);
        for(int i = 0; i < list.size(); i++){
            Select select = new Select();
            select.setText(list.get(i).getItem());
            select.setValue(String.valueOf(list.get(i).getId()));
            jsonArray.add(select);
        }
        return jsonArray;
    }

    public boolean isValidate(String item, Integer projectid, Integer id){
        List<Esensor> list = esensorService.selectBySql("item='" + item + "' and projectid=" + projectid + " and id!=" + id);
        if(list.size() > 0)
            return false;
        else
            return true;
    }
